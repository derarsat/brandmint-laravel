    if (value === 'express') {
        indicaterStyle.value = {
            width: `${experienceWidth.value + 100}px`,
            transform: `translateX(${experienceWidth.value + 112 - 50 - ((experienceWidth.value - expressWidth.value) / 2)}px)`
        }

    } else {
        indicaterStyle.value = {width: `${experienceWidth.value + 100}px`, transform: `translateX(-50px)`}

